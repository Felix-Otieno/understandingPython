# Python data structures are containers that hold data and provide various ways to store, access, and  manipulate that data.
# List , tuples, sets, and dictionaries are types of data structure.

# List is an ordered collection of items.
# List are mutable meaning they can be modified after being created.
# In list index is used to access the items.
# In list you use []
  
myList = ['Alice', 'Bobby', 'Cole', 'David', 1, 2, 3, 4, 5, 0.7] # Create a list
print(myList) # Display the output of the list created by using variable name called myList

# Tuples are similar to list but they are immutable meaning they cannot be modified after being created.
# In tuples you use ()

myTuples = ('Alice', 'Bobby', 'Cole', 'David', 1, 2, 3, 4, 5, 0.7) # Create a tuples
print(myTuples) # Display the output of the tuples created by using variable name called myTuples


# A set in Python is an unordered collection of unique items.
# Sets are mutable, but they cannot contain mutable items.
# In sets you use {}

mySet = {'Alice', 'Bobby', 'Cole', 'David', 1, 2, 3, 4, 5, 0.7} # Create a Set
print(mySet) # Display the output of the set created by using variable name called mySet


# A dictionary in Python is an unordered collection of key-value pairs.
# Dictionary are mutable meaning they can be modified after being created.
# Dictionary are key value pairs and separated by ':' and can  access, modify, and delete elements by the key.
# In dictionary you use {}

myDictionary = { # Create a Dictionary
    'name':'Python',
    'version':3.9,
    'type':'Programming Language'
}
print(myDictionary) # Display the output of the dictionary created by using variable name called myDictionary.